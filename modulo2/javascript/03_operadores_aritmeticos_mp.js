console.log("OPERADORES ARITMETICOS - INMOBILIARIA M A C");
precioCasa = 120000;
precioDepartamento = 80000;
console.log("Suma de precios");
suma = precioCasa + precioDepartamento;
console.log("Total combinado:", suma);
console.log("Diferencia de precios");
resta = precioCasa - precioDepartamento;
console.log("Diferencia:", resta);
console.log("Multiplicacion (ejemplo cantidad * precio)");
multiplicacion = precioDepartamento * 2;
console.log("Precio de 2 departamentos:", multiplicacion);
console.log("Division (ejemplo precio / cuotas)");
division = precioCasa / 12;
console.log("Precio mensual en 12 cuotas:", division);
console.log("Modulo");
modulo = precioCasa % 10000;
console.log("Modulo 10000:", modulo);
console.log("Potencia");
potencia = 2 ** 3;
console.log("2 elevado a 3:", potencia);
console.log("Operadores de Comparacion");
console.log("Igualdad debil ==", "120000"==120000);
console.log("Igualdad estricta ===", "120000"===120000);
console.log("Desigualdad debil !=", "80000"!=80000);
console.log("Desigualdad estricta !==", "80000"!==80000);
console.log("Mayor que >", "120000">100000);
console.log("Menor que <", "80000"<90000);
console.log("Mayor o igual que >=", "120000">=120000);
console.log("Menor o igual que <=", "80000"<=80000);
console.log("Operadores Logicos");
console.log("AND &&", "120000">100000 && "80000"<90000);
console.log("OR ||", "120000"<100000 || "80000"<=80000);
console.log("NEGACION !", !(120000<100000));
